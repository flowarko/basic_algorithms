def f_in_c(value):
    ''' Convert Fahrenheit to Celsius
    :param value: Input Fahrenheit
    :return: Output Celsius
    '''
    return (value - 32) / 1.8


def c_in_f(value):
    ''' Convert Celsius to Fahrenheit
    :param value: Input Celsius
    :return: Output Fahrenheit
    '''
    return value * 1.8 + 32
